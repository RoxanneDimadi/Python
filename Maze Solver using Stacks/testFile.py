from Stack import Stack
from lab04 import *

def test_solveMaze():
    maze = [
        ['+', '+', '+', '+', 'G', '+'],
        ['+', ' ', '+', ' ', ' ', '+'],
        ['+', ' ', ' ', ' ', '+', '+'],
        ['+', ' ', '+', '+', ' ', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', '+', '+', '+', '+', '+']]
    assert solveMaze(maze, 4, 4) == True



    maze2 = [
        ['+', '+', '+', '+', '+'],
        ['+', ' ', '+', 'G', '+'],
        ['+', ' ', '+', '+', '+'],
        ['+', ' ', ' ', ' ', '+'],
        ['+', '+', '+', '+', '+']
    ]
    assert solveMaze(maze2, 3, 3) == False


    maze3 = [
        ['+', '+', '+', '+', '+'],
        ['+', 'G', ' ', ' ', '+'],
        ['+', '+', '+', ' ', '+'],
        ['+', ' ', ' ', ' ', '+'],
        ['+', '+', '+', '+', '+']
    ]
    assert solveMaze(maze2, 3, 3) == False

    maze4 = [
        ['+', '+', '+', '+', '+'],
        ['+', ' ', ' ', ' ', '+'],
        ['+', ' ', 'G', ' ', '+'],
        ['+', ' ', '+', ' ', '+'],
        ['+', '+', '+', '+', '+']
    ]
    assert solveMaze(maze4, 2, 2) == False


